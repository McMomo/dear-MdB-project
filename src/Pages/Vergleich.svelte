  <h3>Was spricht für die A49 und gegen P2?</h3>
  <p>
    Achtung, das sind die Gründe des Bundes für den Bau der A49, allerdings bringen diese für die Region (Stadtallendorf) nur Nachteile:                
  </p>
  <p>  
    Die A49 soll erstens eine Entlastung eines Teilabschnitts von A5/A7 bewirken und zweitens die Strecke zwischen Kassel und Gießen (Dänemark und Italien) um 11,5 km verkürzen.                Diese überregionalen Ziele löst P2 nicht.                 <italic>Allerdings haben diese Argumente für den Durchbau der A49 einen bitteren Beigeschmack:</italic> Die bestehenden Probleme vor Frankfurt (A5) und in Kassel (A7) werden durch die A 49 verschärft (Stau). Die Hoffnung, dass die Umwelt vom Autobahnbau profitiert (CO2-Einsparung), erscheint bei 11,5 km Streckenverkürzung (keine 6 Minuten bei 120km/h) und vorausgesagten 1500 Kfz neu entstehenden Straßenverkehrs fraglich und sind keinesfalls verhältnismäßig.                
  </p>
  <p>
    Kaum eine Firma, (Lokal)politiker oder Anwohner kennt die großen Vorteile für die Region durch P2. Doch P2 wurde bereits 2009 behördlich berechnet und es besteht ein prinzipielles Baurecht!                
